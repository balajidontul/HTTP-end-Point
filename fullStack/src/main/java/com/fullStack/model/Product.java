package com.fullStack.model;

public class Product {

	
	int id;
	String productNameString;
	int productprice ;
	String productCatString;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getProductNameString() {
		return productNameString;
	}
	public void setProductNameString(String productNameString) {
		this.productNameString = productNameString;
	}
	public int getProductprice() {
		return productprice;
	}
	public void setProductprice(int productprice) {
		this.productprice = productprice;
	}
	public String getProductCatString() {
		return productCatString;
	}
	public void setProductCatString(String productCatString) {
		this.productCatString = productCatString;
	}
	
	
	
	
}
